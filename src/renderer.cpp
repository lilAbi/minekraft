bool Renderer::init() {
    shader = Shader("../resource/shader/vertex.glsl","../resource/shader/frag.glsl");

    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);


    glBindVertexArray(VAO);

    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    // position attribute
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    // texture coord attribute

    return true;
}

void Renderer::draw(Player& player, Window& window, std::vector<glm::vec2> drawableChunks) {
    /*
    for (auto& index : drawableChunks) {

    }
    */

    shader.use();
    glBindVertexArray(VAO);

    glm::mat4 projection = glm::perspective(45.0f, static_cast<float>(window.width) / static_cast<float>(window.height), 0.1f, 100.0f);
    shader.setMat4("projection", projection);

    glm::mat4 view = player.getViewMatrix();
    shader.setMat4("view", view);

    glm::mat4 model = glm::mat4(1.0f);
    shader.setMat4("model", model);

    glDrawArrays(GL_TRIANGLES, 0, 36);
}
